    private void deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteMouseClicked
        DefaultTableModel model = (DefaultTableModel) userTable.getModel();
        int row = userTable.getSelectedRow();
        String employee_no = model.getValueAt(row,0).toString();
        try{
   
            JFrame frame = new JFrame ("Delete");
            if (JOptionPane.showConfirmDialog(frame, "Do you wan to delete?","Delete", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_NO_OPTION){
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/attendancemsfjava","root","");
                Statement stmt = con.createStatement(); 
                String query = "DELETE employee,users from employee inner join users  WHERE employee.employee_no ='"+employee_no+"' and users.username ='"+employee_no+"'";
                stmt.executeUpdate(query);
                JOptionPane.showMessageDialog(null, "SUCCESSFULLY DELETED","Alert", JOptionPane.WARNING_MESSAGE);
                 model.removeRow(row);
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage(),"Alert", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteMouseClicked

