    public void fetchData(){
        int count =0;
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/attendancemsfjava" ,"root","");
            Statement stmt = (Statement) con.createStatement();
            ResultSet datas = stmt.executeQuery("SELECT * FROM users");
            DefaultTableModel model = (DefaultTableModel) userTable.getModel();
            
            while (datas.next()){
               count = 1;
               model.addRow(new Object[]{datas.getString("username"),datas.getString("password").hashCode(), datas.getInt("age"),datas.getString("mobile_no"),datas.getString("gender")});
            }
             if(count == 0){
                JOptionPane.showMessageDialog(null,"No data found!." , "Alert", JOptionPane.WARNING_MESSAGE);
                
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage(),"Alert", JOptionPane.WARNING_MESSAGE);
        }
    }
    
   
    


